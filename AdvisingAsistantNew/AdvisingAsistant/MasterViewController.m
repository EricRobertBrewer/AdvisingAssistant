- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    if ([self.title isEqualToString:@"Graduation Requirements"] && indexPath.row == 0) {
    
        NSArray *ge = [[NSArray alloc] initWithObjects:@"Section A", @"Section B", @"Section C", @"Section D", @"Section E", @"Ethnic Studies", @"Lab Requirement", nil];
    
        MasterViewController *GETable = [[MasterViewController alloc] initWithValues:ge andTitle:@"GE Courses"];
    
        [self.navigationController pushViewController:GETable animated:YES];
    
        [GETable release];
        [ge release];
    }
}

